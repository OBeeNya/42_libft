#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*a;

	if (!lst)
		return (0);
	a = lst->next;
	i = 1;
	while (a)
	{
		i++;
		a = a->next;
	}
	return (i);
}
